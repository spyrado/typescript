// tsconfig.json dentro de "CompilerOptions" -> "target" <- aqui mudamos para a versão que desejamos.
// exemplo: "target": "es5"
// o código todo vai compilar para a versão ES5 e dar a compatibilidade do ES5, se quisessemos atualizar para a versao mais nova ES6 era só mudar para ES6
// e assim por diante, claro que mudando a versao o código para de dar compatibilidade para navegadores de versões mais antigas
// porem tudo vai da necessidade e objetivo do cliente do que ele busca atender e etc.